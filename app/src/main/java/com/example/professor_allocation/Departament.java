package com.example.professor_allocation;

public class Departament {
    private int id;
    private String name;

    public Departament() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
